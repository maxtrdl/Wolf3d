#include "libft.h"

void	ft_putnbr(int n)
{
	long long int n1;

	n1 = (long long int)n;
	if (n1 < 0)
	{
		ft_putchar('-');
		n1 = n1 * (-1);
	}
	if (n1 >= 10)
	{
		ft_putnbr(n1 / 10);
	}
	ft_putchar((n1 % 10) + '0');
}
