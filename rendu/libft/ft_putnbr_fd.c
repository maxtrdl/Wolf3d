#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	n1;

	n1 = (long int)n;
	if (n1 < 0)
	{
		n1 = n1 * (-1);
		ft_putchar_fd('-', fd);
	}
	if (n1 >= 10)
		ft_putnbr_fd(n1 / 10, fd);
	ft_putchar_fd((n1 % 10) + '0', fd);
}
